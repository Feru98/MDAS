package CopyManager;

public class Copy {
   private String _isbn;
   private float _price;
   
   

   public String getISBN() {
	   return _isbn;
   }

   public void setISBN(String _isbn) {
	   this._isbn = _isbn;
   }

   public float getPrice() {
	   return _price;
   }

   public void setPrice(float _price) {
	   this._price = _price;
   }
}
